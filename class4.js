/**
 * Incrementing
 */
var number = 0;
console.log(number);

number = number + 1;
console.log(number);

number += 1; // 1+1;
console.log(number); // short have version;

number++;
console.log(number); // will only increase by one;

/**
 * Decrementing
 */
var value = 100;
console.log(value);

value = value - 1;
console.log(value);

value -= 1;
console.log(value);

value--;
console.log(value); // will only decrease by one;